{
}

//==============================================================================
//
//==============================================================================
PosixTimestamp& PosixTimestamp::operator=(const timespec& tp)
{
    this->tp.tv_sec  = tp.tv_sec;
    this->tp.tv_nsec = tp.tv_nsec;

    return *this;
}

