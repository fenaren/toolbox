#include "PosixClock.hpp"

#include "PosixTimespec.hpp"

//==============================================================================
//
//==============================================================================
PosixClock::PosixClock(clockid_t clk_id) :
    clk_id(clk_id)
{
}

//==============================================================================
//
//==============================================================================
PosixClock::~PosixClock()
{
}

//==============================================================================
//
//==============================================================================
int PosixClock::getTime(PosixTimespec& ts) const
{
    timespec tp;
    int cg_ret = clock_gettime(clk_id, &tp);

    ts = tp;

    return cg_ret;
}
